package prot;

class Fpoint {
    private float x;
    private float y;
    private float z;

    Fpoint(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    float getX() {
        return x;
    }

    float getY() {
        return y;
    }

    float getZ() {
        return z;
    }
}
